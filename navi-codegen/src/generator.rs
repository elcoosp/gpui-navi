use crate::config::NaviConfig;
use crate::scanner::scan_routes;
use anyhow::Result;
use std::fs;
use std::path::Path;
use std::collections::BTreeSet;

fn sanitize_ident(s: &str) -> String {
    s.replace(['(', ')', '-', '.', '$', '{', '}'], "_")
}

pub fn generate_route_tree(config: &NaviConfig) -> Result<String> {
    let routes = scan_routes(config)?;
    ensure_stub_files_exist(config, &routes)?;

    let mut route_nodes = String::new();
    let mut module_decls = BTreeSet::new();
    let mut register_calls = String::new();

    for route in &routes {
        let parent_field = if let Some(ref parent) = route.parent {
            format!("Some(\"{}\".into())", parent)
        } else {
            "None".into()
        };

        let module_path = &route.module_name;
        let route_type = &route.route_type_name;
        let file_path = route.relative_path.to_str().unwrap();

        // Sanitize module identifier
        let mod_ident = sanitize_ident(&module_path.replace("::", "_"));
        module_decls.insert(format!("#[path = \"routes/{}\"] pub mod {};", file_path, mod_ident));

        let node_block = format!(
            r#"
    {{
        let mut node = {mod_ident}::{route_type}::build_node();
        node.parent = {parent};
        tree.add_route(node);
    }}
"#,
            mod_ident = mod_ident,
            route_type = route_type,
            parent = parent_field,
        );

        if let Some(feature) = &route.cfg_feature {
            route_nodes.push_str(&format!(
                r#"
#[cfg(feature = "{feature}")]
{node_block}"#,
                feature = feature,
                node_block = node_block,
            ));
        } else {
            route_nodes.push_str(&node_block);
        }

        // Register call
        let register_line = if let Some(feature) = &route.cfg_feature {
            format!("#[cfg(feature = \"{}\")] {}::{}::register(cx);\n", feature, mod_ident, route_type)
        } else {
            format!("{}::{}::register(cx);\n", mod_ident, route_type)
        };
        register_calls.push_str(&register_line);

        // If route is a 404 (not found) route, also register under __not_found_* keys
        if route.is_not_found {
            let not_found_register = if let Some(feature) = &route.cfg_feature {
                format!("#[cfg(feature = \"{}\")] {}::{}::register(cx);\n", feature, mod_ident, route_type)
            } else {
                format!("{}::{}::register(cx);\n", mod_ident, route_type)
            };
            register_calls.push_str(&not_found_register);
        }

    }

    let module_decls_str = module_decls.into_iter().collect::<Vec<_>>().join("\n");

    let output = format!(
        r#"// AUTO-GENERATED by navi-codegen - DO NOT EDIT

// Module declarations for discovered routes
{module_decls}

/// Build the route tree with all discovered routes.
pub fn build_route_tree() -> navi_router::RouteTree {{
    let mut tree = navi_router::RouteTree::new();
{route_nodes}
    tree
}}

/// Register all route components and loaders.
pub fn register_routes(cx: &mut gpui::App) {{
{register_calls}
}}
"#,
        module_decls = module_decls_str,
        route_nodes = route_nodes,
        register_calls = register_calls,
    );

    Ok(output)
}

fn ensure_stub_files_exist(
    config: &NaviConfig,
    routes: &[crate::scanner::RouteInfo],
) -> Result<()> {
    let routes_dir = Path::new(&config.routes_directory);
    for route in routes {
        let file_path = routes_dir.join(&route.relative_path);
        if !file_path.exists() {
            continue;
        }
        let content = fs::read_to_string(&file_path)?;
        if content.contains("define_route!") {
            continue;
        }

        let stub = generate_stub_content(route);
        fs::write(&file_path, stub)?;
        println!("Generated stub for {:?}", route.relative_path);
    }
    Ok(())
}

fn generate_stub_content(route: &crate::scanner::RouteInfo) -> String {
    let route_type = &route.route_type_name;
    let pattern = &route.route_pattern;
    let is_layout = route.is_layout;
    let is_index = route.is_index;

    let mut content = format!(
        r#"// Generated stub for route {route_type}
use gpui::prelude::*;
use gpui::*;
use navi_macros::define_route;
use navi_router::RouteDef;

#[derive(Clone, IntoElement)]
struct Placeholder;

impl RenderOnce for Placeholder {{
    fn render(self, _: &mut Window, _: &mut App) -> impl IntoElement {{
        div().child("Placeholder for {route_type}")
    }}
}}

"#,
        route_type = route_type,
    );

    if let Some(feature) = &route.cfg_feature {
        content.push_str(&format!("#[cfg(feature = \"{}\")]\n", feature));
    }

    content.push_str(&format!(
        r#"define_route!(
    {route_type},
    path: "{pattern}",
    is_layout: {is_layout},
    is_index: {is_index},
    component: Placeholder,
);
"#,
        route_type = route_type,
        pattern = pattern,
        is_layout = is_layout,
        is_index = is_index,
    ));

    content
}

pub fn write_route_tree(config: &NaviConfig) -> Result<()> {
    let content = generate_route_tree(config)?;
    let out_path = Path::new(&config.generated_route_tree);
    if let Some(parent) = out_path.parent() {
        fs::create_dir_all(parent)?;
    }
    fs::write(out_path, content)?;
    Ok(())
}
