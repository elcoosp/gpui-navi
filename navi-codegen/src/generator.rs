use crate::config::NaviConfig;
use crate::scanner::scan_routes;
use anyhow::Result;
use std::fs;
use std::path::Path;
use std::collections::BTreeSet;

fn sanitize_ident(s: &str) -> String {
    let replaced = s.replace(['(', ')', '-', '.', '$', '{', '}'], "_");
    let mut result = String::new();
    let mut last_was_underscore = false;
    for ch in replaced.chars() {
        if ch == '_' {
            if !last_was_underscore {
                result.push('_');
                last_was_underscore = true;
            }
        } else {
            result.push(ch);
            last_was_underscore = false;
        }
    }
    result.trim_end_matches('_').to_lowercase()
}

fn is_snake_case(s: &str) -> bool {
    s.chars().all(|c| c.is_ascii_lowercase() || c.is_ascii_digit() || c == '_')
}

pub fn generate_route_tree(config: &NaviConfig) -> Result<String> {
    let routes = scan_routes(config)?;

    let mut route_nodes = String::new();
    let mut module_decls = BTreeSet::new();
    let mut register_calls = String::new();

    for route in &routes {
        let parent_field = if let Some(ref parent) = route.parent {
            format!("Some(\"{}\".into())", parent)
        } else {
            "None".into()
        };

        let module_path = &route.module_name;
        let route_type = &route.route_type_name;
        let file_path = route.relative_path.to_str().unwrap();

        let mod_ident = sanitize_ident(&module_path.replace("::", "_"));
        let mod_decl = if is_snake_case(&mod_ident) {
            format!("#[path = \"routes/{}\"] pub mod {};", file_path, mod_ident)
        } else {
            format!("#[allow(non_snake_case)]\n#[path = \"routes/{}\"] pub mod {};", file_path, mod_ident)
        };
        module_decls.insert(mod_decl);

        let node_block = format!(
            r#"
    {{
        let mut node = {mod_ident}::{route_type}::build_node();
        node.parent = {parent};
        tree.add_route(node);
    }}
"#,
            mod_ident = mod_ident,
            route_type = route_type,
            parent = parent_field,
        );

        if let Some(feature) = &route.cfg_feature {
            route_nodes.push_str(&format!(
                r#"
#[cfg(feature = "{feature}")]
{node_block}"#,
                feature = feature,
                node_block = node_block,
            ));
        } else {
            route_nodes.push_str(&node_block);
        }

        let register_line = if let Some(feature) = &route.cfg_feature {
            format!("#[cfg(feature = \"{}\")] {}::{}::register(cx);\n", feature, mod_ident, route_type)
        } else {
            format!("{}::{}::register(cx);\n", mod_ident, route_type)
        };
        register_calls.push_str(&register_line);
    }

    let module_decls_str = module_decls.into_iter().collect::<Vec<_>>().join("\n");

    let output = format!(
        r#"// AUTO-GENERATED by navi-codegen - DO NOT EDIT

// Module declarations for discovered routes
{module_decls}

/// Build the route tree with all discovered routes.
pub fn build_route_tree() -> navi_router::RouteTree {{
    let mut tree = navi_router::RouteTree::new();
{route_nodes}
    tree
}}

/// Register all route components and loaders.
pub fn register_routes(cx: &mut gpui::App) {{
{register_calls}
}}
"#,
        module_decls = module_decls_str,
        route_nodes = route_nodes,
        register_calls = register_calls,
    );

    Ok(output)
}

